var sweety="sweety";
var cutie="cutie";
console.log("==========Swap using two variables==========");
console.log("--Before swap--");
console.log(sweety,cutie);
var temp=sweety;
sweety=cutie;
cutie=temp;
console.log("--After swap--");
console.log(sweety,cutie);

