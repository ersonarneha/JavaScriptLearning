console.log(`=================5 to 15 by incrementing 1 using while loop========`);
var index=5;
while (index<=15) {
    console.log(index);
    index++;
}
console.log(`=================50 to 40 by decrementing 1 using while loop========`);
var index=50;
while (index>=40) {
    console.log(index);
    index--;
}
console.log(`=================Table of 5 using do while loop========`);
var index=5;
do {console.log(index);
   index= index+5;
} 
while (index<=50)
